<h3><a href="#">Revelation</a></h3>
<h6>Written by <a href="#">Paul Manning</a> on October 16, 1990</h6>

<pre>

Touch me with your eyes
Dispel a thousand doubts
Hold me in your arms
Else what is life about
Be with me tonight
To soothe my troubled mind
Your presence to unfold me
Your nature like a smile

The boy in me awakened
Curiosity to consume
Like an explorer of new islands
I give myself to you
And the feelings of acceptance
Warm me like a fire
Revelation bursts upon me
Your love is my desire
 
</pre>

