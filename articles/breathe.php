<h3><a href="#">Irrisistible Combination</a></h3>
<h6>Written by <a href="#">Paul Manning</a> on June 10, 1996</h6>

<pre>

I breathe in
Your skin
So soft
And warm
My eyes
Dilate with delight

Your touch
Is soothing
Undemanding
Gently caressing
My doubts away
So tenderly

Vulnerability and
Carnal pleasure
Mixed together
Expressed within
Your eyes
Irresistible combination

</pre>

